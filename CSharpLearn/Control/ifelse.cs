namespace CSharpLearn.Control 
{
    public class Ifelse 
    {
        public static void conditions() 
        {
            int x = 18;

            if (18 > x) {
                Console.WriteLine("yes");
            } else if (18 == x) {
                Console.WriteLine("maybe");
            } else {
                Console.WriteLine("no");
            }
        }
    }
}